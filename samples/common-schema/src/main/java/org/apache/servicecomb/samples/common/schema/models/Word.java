package org.apache.servicecomb.samples.common.schema.models;

public class Word {
  private String owner;

  private int length;

  public String getOwner() {
    return owner;
  }

  public void setOwner(String owner) {
    this.owner = owner;
  }

  public int getLength() {
    return length;
  }

  public void setLength(int length) {
    this.length = length;
  }
}
